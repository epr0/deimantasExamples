package lt.sdacademy.fundamentals.loop.combined;

public class Example2 {

    public static void main(String args[]){

        for(int k = 0; k<=5; k++) {
            System.out.print("*");
        }

        System.out.println();

        for(int i = 0; i<5; i++) {
            System.out.println("*    *");
        }

        for(int j = 0; j<=5; j++) {
            System.out.print("*");
        }
    }
}
