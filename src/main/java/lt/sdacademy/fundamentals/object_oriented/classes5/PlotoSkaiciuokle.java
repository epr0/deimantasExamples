package lt.sdacademy.fundamentals.object_oriented.classes5;

public class PlotoSkaiciuokle {

    public int skaiciuotiPlota(int plotis, int ilgis) {
        return plotis * ilgis;
    }

    public double skaiciuotiStogoPlota(int plotis, double kampas) {
        return kampas * 2 + plotis * 2;
    }
}
